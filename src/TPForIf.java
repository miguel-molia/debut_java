public class TPForIf {

    public static void main(String[] args) {

        for (int i = 0; i < 21; i++) {

            System.out.println(i);

            if (i == 10) {

                System.out.println("programme exécuté à moitié");
            }
        }
        System.out.println("programme exécuté complétement");
    }
}