public class WhileExo {
    public static void main(String[] args) {

        int i = 1;

        while (i < 1000001) {

            System.out.println("Poney numéro " + i);

            i++;

        }


        i = 1000000;

        while (i > 1) {

            System.out.println("Poney numéro " + i);

            i--;

        }


    }
}